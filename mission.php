            <p>從聖經的教訓及預言觀之，在各個時代，宗派林立，異端邪說都用各種不同的方式出現，這是撒旦為要阻擋福音，混亂真道，引人離開基督陷入沈淪的計謀，所放下的絆腳石。</p>

            <p>聖經是基督真理的唯一準則，凡是與神所默示的《聖經》不相符，與神的聖靈所建立純正使徒教會所傳不合，就是被神視為----異端邪說，處於撒旦的工作。所謂異端，是指打著基督教招牌，竊用聖經某些話語，再加以歪曲甚至竄改;或者，把某人的著作及其思想，奉為經典甚至信條。</p>

            <p>新約聖經二十七卷內，除了一卷腓利門書外，其余二十六卷都提到要防備假先知、假師傅，要棄絕各種偏離真理之道的錯誤。在主耶穌快再來的時候，異端錯謬更是猖狂，提摩太前書四章七節告訴我們：”聖靈明說，在後來的時候，必有人離棄真道，聽從那引誘人的邪靈和鬼魔的道理”。因此，每一個基督徒都應當明白聖經真理，持定所承認的道，防備一切異端。凡有如下特征的宗派信仰當堅決抵制，以免受迷惑離開正道。</p>
            <p>
              <ol>
                <li>否定《聖經》的權威，說什麼聖經已經過時了，不能全信等</li>
                <li>加添和減少聖經</li>
                <li>濫解、曲解聖經</li>
                <li>所傳的道是而又非，沒有一定的教義標準</li>
                <li>高舉人，樹立偶像，冒稱基督或使者</li>
                <li>專以啟示作為真理權威</li>
                <li>明顯有一種邪靈的力量在運行控制</li>
                <li>嚴重者產生惡果，成為邪教</li>
                <li>別有用心的發熱心</li>
                <li>說假預言，諸如“主要來的日子”，“世界末日”等</li>
              </ol>
            </p>
            <p><strong>純正真實的信仰必定是：承認聖父、聖子、聖靈三而一的獨一真神。</strong></p>
            <p><strong>聖父</strong>是創造一切的主，是靈，是獨一全智的至高者，是慈愛、信實、公義、聖潔的全能者，是從亙古到永遠、永不改變的神。</p>

            <p><strong>聖子</strong>耶穌基督，是神的獨生子，受神差遣來到世間;祂透過道成肉身，將神完全表明出來;祂是藉著童女受聖靈感孕而生的聖者，是神而人的奇妙位格，既有神性又有人性;祂是亞當之外的第二人;在祂裏面沒有罪，祂外面肉身也沒有罪，祂是純全聖善、無邪惡、不受玷汙的義者。祂釘在十字架上，將自己獻與神作全人類的贖罪祭，祂的寶血洗凈了人的罪，藉著祂一次的受死、獻祭，就成就了永遠的救贖;他死後第三天從死裏復活，將自己的生命賜給信祂的人;如今祂在天上作我們的元首、中保、大祭司，使我們從祂得恩典，得豐富，被保守，被拯救到底;祂還要再來，接我們到祂那裏去，使我們完全改變得象祂，進入祂的榮耀;在祂裏面，萬有必同歸於一。</p>

            <p><strong>聖靈</strong>是神的靈，受祂感動，我們悔改相信而得蒙重生。祂又運行在我們心中靈裏，引導我們進入真理，成為聖潔，教導我們如何立誌行事，如何事奉交通，如何忍耐等候主再來。</p>

            <p><strong>聖父、聖子、聖靈</strong>三而一的神，是主耶穌升天前親自宣告的絕對真理，是歷代教會的信仰基礎，也是信徒敬虔的奧秘所在，因此不可妄加竄改。相信三而一真神，才是真正的基督教信仰。</p>

            <p>純正的信仰相信，主耶穌在十字架上只一次就成全了永遠的救贖，祂的寶血永遠有效，使我們稱義、得救、成為聖潔。得救的人不但脫離了律法的定罪和咒詛，而且不再死守律法，而是憑著主的無罪的聖潔生命，靠著聖靈治死肉體的行為，藉恩膏的教訓除去不敬虔的心和世俗的情欲，在今世自守、公義、敬虔地度日，活出基督的生命，作聖潔無過的人。我們生活的原則，不是去謹守律法中的規條，而是憑生命聖靈的律在基督裏作新造的人。</p>

            <p>純正的信仰必然相信聖經乃是出予神的默示，聖經的中心意義是見證基督。在新舊約全書中，舊約聖經的律法是指向基督，叫人因信基督得到義;基督則是律法的總結，讓信的人不再死守律法而是蒙受恩典。舊約的預言、預表，在基督裏面完全得到應驗。新約聖經通過使徒之手記載著神子的曉喻，它高於一切律法和先知，是真理的聖靈所啟示的話語，使我們明白神的心意，成全神的旨意，認識神的奧秘，在基督裏滿得神的豐富和屬靈生命的實際，使我們得以成聖並滿有榮耀的盼望。</p>

            <p>真正的基督教會都相信全部聖經是聖靈默示人所寫的，都要成全。感謝神賜給我們這部寶貴的聖經。我們知道自己所信的是誰，知道自己所信的是什麼，才能在所信的道上非常清醒和剛強有力，從而真正識別和抵制一切否認甚至篡改聖經的異端。</p>
